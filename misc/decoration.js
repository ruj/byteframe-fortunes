//------------------------------------------------------------------------------ PoolProcedure
pool = (pool, length = 1, join = '', elements = []) => {
  if (!pool.hasOwnProperty('index')) {
    pool.index = 0;
  }
  for (; length > 0; length--) {
    if (pool.index === 0) {
      shuffle_array(pool);
    }
    elements.push(pool[pool.index]);
    if (++pool.index == pool.length) {
      pool.index = 0;
    }
  }
  if (join !== null) {
    return elements.join(join);
  }
  return elements;
};
